    <p className="mb-6">Absolutely, when you calculate true cost. A $50 wooden toy used for 8 years costs $6.25 per year. A $15 plastic toy replaced 3 times over 2 years costs $22.50 per year. Wooden toys also retain resale value (40-60%), can be passed to siblings, and become family heirlooms. Beyond economics, wooden toys offer better sensory experiences, no harmful chemicals, and environmental sustainability. The upfront cost is higher, but the value over time makes wooden toys the economical choice.</p>

