    <p className="mb-6">Inspect for structural integrity first. Check that pieces are securely attached, no splinters or sharp edges, and surfaces are smooth. Verify the finish—if paint is chipping or flaking, pass (could be lead paint on older toys). Look for solid wood rather than laminated or composite. Test moving parts—wheels should roll smoothly, pieces should fit together properly. Quality wooden toys age beautifully and remain safe for decades, making secondhand an excellent option. Avoid toys with missing pieces unless you can replace them or the toy works well without them.</p>

